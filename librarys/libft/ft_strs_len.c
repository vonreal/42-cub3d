#include "libft.h"

int		ft_strs_len(char **strs)
{
	int		i;
	int		j;

	i = 0;
	while (strs[i] != NULL)
	{
		j = 0;
		while (strs[i][j] != '\0')
			j++;
		i++;
	}
	return (i);
}
