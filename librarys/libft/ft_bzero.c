#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*k;

	i = 0;
	k = s;
	while (i < n)
	{
		k[i] = '\0';
		i++;
	}
	return (s);
}
